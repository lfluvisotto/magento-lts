class Adyen_Subscription_Model_Total_SubscriptionFee_Creditmemo extends Mage_Sales_Model_Order_Creditmemo_Total_Abstract
{
    public function collect(Mage_Sales_Model_Order_Creditmemo $creditmemo)
    {
        $creditmemo->setGrandTotal($creditmemo->getGrandTotal()+$creditmemo->getSubscriptionFeeAmount());
        $creditmemo->setBaseGrandTotal($creditmemo->getBaseGrandTotal()+$creditmemo->getBaseSubscriptionFeeAmount());
        return $this;
    }
}