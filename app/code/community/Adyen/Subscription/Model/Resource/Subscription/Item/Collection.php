class Adyen_Subscription_Model_Resource_Subscription_Item_Collection extends Mage_Core_Model_Resource_Db_Collection_Abstract
{
    protected function _construct()
    {
        $this->_init('adyen_subscription/subscription_item');
    }

    public function addRowTotalInclTax()
    {
        $this->getSelect()->columns(['row_total_incl_tax' => new Zend_Db_Expr('price_incl_tax * qty')]);

        return $this;
    }
}
